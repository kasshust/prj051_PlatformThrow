using System.Collections;
using System.Collections.Generic;
using UnityEngine;
using UnityEngine.UI;
using System;
using UniSearchableEnumAttribute;

public class StoryManager : SingletonMonoBehaviourFast<StoryManager>
{
    protected override void Awake()
    {
        base.Awake();
        DontDestroyOnLoad(this.gameObject);
    }

    [SerializeField] private Text Debug_CurrentStage;
    [SerializeField] private Text Debug_CurrentSubStage;

    

    #region 外部から読み込み
    // タイトル　マップのロード

    public void LoadTitleScene(int index = 0)
    {
        LoadStage(StoryStage.StageDefault, "TitleScene", StorySubStage.SubStage000, 0);
    }

    public void LoadMapScene(StoryStage stage, StorySubStage subStage = StorySubStage.SubStage000, int index = 0)
    {
        LoadStage(stage, "MapScene", subStage, index);
    }

    public void LoadMapScene(int index = 0)
    {
        LoadScene("MapScene", index);
    }

    public void LoadGameOverScene(int index = 0)
    {
        LoadStage(StoryStage.StageDefault, "GameOver", StorySubStage.SubStage000, 0);
    }

    #endregion


    private void LoadScene(string SceneName, int index = 0)
    {
        TransitionManager.instance.changeScene(SceneName, index);
    }

    private void LoadStage(StoryStage stage, string SceneName, StorySubStage subStage = StorySubStage.SubStage000, int index = 0)
    {
        ChangeStage(stage, subStage);
        TransitionManager.instance.changeScene(SceneName, index);
    }

    public void ChangeStage(StoryStage stage, StorySubStage subStage = StorySubStage.SubStage000)
    {
    }

    public void ChangeSubStage(StorySubStage subStage)
    {
    }

    public StoryStages GetStages() {
        StoryStages stages;
        stages.m_Stage      =    GetStage();
        stages.m_SubStage   =    GetSubStage();
        return stages;
    }

    private StoryStage GetStage()
    {
        return StoryStage.Stage000;
    }
    private StorySubStage GetSubStage()
    {
        return StorySubStage.SubStage001;
    }

    #region ストーリーフェーズ

    [SerializeField, ReadOnlyAttribute]
    private StoryPhase m_Phase;

    [SerializeField]
    private PhasePlayState m_PlayState;

    public void LoadPhase(StoryPhase phase, int index = 0, PhasePlayState state = PhasePlayState.Default)
    {
        SetState(state);
        ChangePhase(phase);
        TransitionManager.instance.changeScene("EventScene", index);
    }

    public void LoadPhase(StoryPhase phase, string name, PhasePlayState state = PhasePlayState.Default)
    {
        SetState(state);
        ChangePhase(phase);
        TransitionManager.instance.changeScene("EventScene", name);
    }


    private void ChangePhase(StoryPhase phase) {
        m_Phase = phase;
    }

    private void LoadPhase(StoryPhase phase, string SceneName, int index = 0) {
        ChangePhase(phase);
        TransitionManager.instance.changeScene(SceneName, index);
    }

    public StoryPhase GetPhase() {
        return m_Phase;
    }

    public PhasePlayState GetState() {
        return m_PlayState;
    }

    public void SetState(PhasePlayState s)
    {
        m_PlayState = s;
    }

    #endregion

}

